#include "libft.h"

int	ft_isalnum(int ch)
{
	if (ch <= 'Z' && ch >= 'A')
		return (1);
	if (ch <= 'z' && ch >= 'a')
		return (1);
	if (ch >= '0' && ch <= '9')
		return (1);
	return (0);
}
